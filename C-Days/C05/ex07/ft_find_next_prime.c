int	ft_is_prime(int nb)
{
	int	i;

	if (nb == 2147483647)
	{
		return (1);
	}
	i = 2;
	while (i < nb)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}

int	ft_find_next_prime(int nb)
{
	if (nb <= 2)
		return (2);
	while (!ft_is_prime(nb))
	{
		nb += 1;
	}
	return (nb);
}
